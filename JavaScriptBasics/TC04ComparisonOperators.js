//Comparison Operators
/* == - Equality Operator checks for equality of value, but not type
=== - Strict Equality Operator checks for equality of value and type (strict equality)
!= - Inequality Operator checks for inequality of value, but not type 
!== - Strict Inequality Operator checks for inequality of value or type (strict inequality)

*/
let a = 5;
let b = '10';

console.log(a == b);  // true, because == performs type coercion and converts b to a number before comparison